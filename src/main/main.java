    public Icon getIcono(JLabel jlb, File archivo) {
        jlb.setText("");
        ImageIcon image = new ImageIcon(archivo.getAbsolutePath());
        Icon icono = new ImageIcon(image.getImage().getScaledInstance(jlb.getWidth(), jlb.getHeight(), Image.SCALE_REPLICATE));
        return icono;
    }


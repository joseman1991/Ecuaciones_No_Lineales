    private void inicializar() {
        setLocationRelativeTo(null);
        setResizable(false);
        
        iconoBiseccion.setIcon(getIcono(iconoBiseccion, new File("recursos/biseccion.png")));
        iconosecante.setIcon(getIcono(iconosecante, new File("recursos/secante.png")));
        iconoFalsa.setIcon(getIcono(iconoFalsa, new File("recursos/falsa.png")));
        iconoNewton.setIcon(getIcono(iconoNewton, new File("recursos/newton.png")));
        iconoSalir.setIcon(getIcono(iconoSalir, new File("recursos/salir.png")));
        
        pb = new PanelBisección();
        ps = new PanelSecante();
        pfp= new PanelFalsaPosicion();
        pn=new PanelNewton();
        
        ps.setSize(destop.getWidth() - 4, destop.getHeight() - 4);
        pfp.setSize(destop.getWidth() - 4, destop.getHeight() - 4);
        pn.setSize(destop.getWidth() - 4, destop.getHeight() - 4);
        pb.setSize(destop.getWidth() - 4, destop.getHeight() - 4);
        
        pfp.setLocation(1, 1);
        pn.setLocation(1, 1);
        ps.setLocation(1, 1);        
        pb.setLocation(1, 1);
        
        destop.add(pb);
        destop.add(pfp);
        destop.add(pn);
        destop.add(ps);
        
        pb.setVisible(false);
        ps.setVisible(false);
        pn.setVisible(false);
        pfp.setVisible(false);

    }

